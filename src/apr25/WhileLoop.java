package apr25;

public class WhileLoop {

    public static void main(String[] args) {
        int x = 1;    // initialisation
        while (x > 10){ // condition   1 > 10 true
            // statement
            System.out.println(x); // 1 2 3
            x++; // 4
        }
        System.out.println("ending the loop");
    }
}
